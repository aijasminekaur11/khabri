    def _format_telegram_message(self, news_items: list) -> str:
        """Format news items for Telegram."""
        message = "🌅 Good Morning!\n\n"
        message += f"📰 {len(news_items)} news items\n\n"
        for item in news_items:
            message += f"• {item.get('title', 'No title')}\n"
        message += "\n📧 Details in email"
        return message


